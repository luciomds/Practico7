    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        //Construir
        int legajo = Integer.parseInt(jtLegajo.getText());
        String nombre = jtNombre.getText();
        String apellido = jtApellido.getText();
        
        
        Alumno a = new Alumno(legajo, apellido, nombre);
        
           
        
        //Guardar
        
        if(!(Menu.getAlumnos().contains(a))){
            Menu.getAlumnos().add(a);
            JOptionPane.showMessageDialog(this, "Alumno registrado.");
        }else{
            JOptionPane.showMessageDialog(this, "El alumno ya se encuentra registrado.");
        }
        
        limpiar();
    }//GEN-LAST:event_jbGuardarActionPerformed

